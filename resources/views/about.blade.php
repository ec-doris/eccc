                    <h2 class="ecl-u-type-heading-3" id="mission">Mission</h2>
                    <p class="ecl-u-type-paragraph-m">The <strong>European Cybersecurity Competence Centre (ECCC)</strong>, together with the <strong>Network of National Coordination Centres (NCCs)</strong>, is Europe’s new framework to support innovation and industrial policy in cybersecurity. This ecosystem will strengthen the capacities of the cybersecurity technology community, shield our economy and society from cyberattacks, maintain research excellence and reinforce the competitiveness of EU industry in this field.</p>
                    <p class="ecl-u-type-paragraph-m">The ECCC will be located in Bucharest. Alongside Member States and the cybersecurity technology community, it will develop and implement a common agenda for technology innovation. It will aim to deploy this in areas of public interest and in businesses, in particular SMEs. </p>
                    <p class="ecl-u-type-paragraph-m">The Centre and the Network together will enhance our <strong>technological sovereignty through joint investment in strategic cybersecurity projects.</strong></p>



                    <h2 class="ecl-u-type-heading-2" id="tasks">Tasks</h2>
                    <p class="ecl-u-type-paragraph-m">The Centre and the Network will <strong>make strategic investment
                            decisions and pool resources</strong> from the EU, its Member States and, indirectly, the industry, to
                        improve and strengthen technology and industrial cybersecurity capacities, enhancing the EU’s
                        open strategic autonomy. The Centre will play a key role in delivering on the ambitious
                        cybersecurity objectives of the <a href="https://digital-strategy.ec.europa.eu/en/activities/digital-programme" class="ecl-link ecl-link--default" aria-label="Digital Europe Programme">Digital Europe Programme</a> and <a href="https://ec.europa.eu/info/research-and-innovation/funding/funding-opportunities/funding-programmes-and-open-calls/horizon-europe_en" class="ecl-link ecl-link--default" aria-label="Horizon Europe">Horizon Europe</a> programmes.</p>
                    <p class="ecl-u-type-paragraph-m">The Centre together with the Network will support the <strong>deployment
                            of innovative cybersecurity solutions.</strong> It will also facilitate <strong>collaboration and the sharing of
                            expertise and capacities</strong> among all relevant stakeholders, in particular research and industrial
                        communities, as well as public authorities.</p>

                    <h2 class="ecl-u-type-heading-2" id="regulation-establishing-the-ECCC">Regulation establishing the ECCC</h2>
                    <p class="ecl-u-type-paragraph-m">On 8 June 2021, <a href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=CELEX:32021R0817" class="ecl-link ecl-link--default" aria-label="the Regulation establishing the European Cybersecurity Competence Centre and Network was published.">the Regulation establishing the European
                            Cybersecurity Competence Centre and Network was published.</a></p>
                    <p class="ecl-u-type-paragraph-m">During the negotiations that led to the adoption of the
                        Regulation, the EU co-legislators (the European Parliament and the Council) agreed in particular
                        on a <strong>co-financing approach</strong> by which Member States commit to contributing to the work of the
                        Centre and the Network, while keeping individual Member States’ contributions voluntary.</p>
                    <p class="ecl-u-type-paragraph-m">The co-legislators also agreed on the distribution of <strong>voting
                            rights between Member States and the Union in the Centre’s Governing Board</strong>, giving the Union
                        particular voting powers on decisions affecting the EU budget.</p>
                    <p class="ecl-u-type-paragraph-m">The ECCC is a new EU body established under articles 173(3) and
                        188(1) of the Treaty on the Functioning of the European Union (TFEU).</p>
                    <h2 class="ecl-u-type-heading-2" id="organisation">Organisation</h2>
                    <p class="ecl-u-type-paragraph-m">The ECCC is currently being set up. The Commission will ensure the
                        functioning of the ECCC until this new EU body can operate autonomously.</p>
                    <p class="ecl-u-type-paragraph-m">The ECCC administrative and governance structure includes:
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item">a <strong>Governing Board</strong> which provides strategic orientation and
                            oversees ECCC activities;
                        </li>
                        <li class="ecl-unordered-list__item">an <strong>Executive Director</strong> who is the ECCC’s legal
                            representative and is responsible for its day-to-day management;
                        </li>
                        <li class="ecl-unordered-list__item">a <strong>Strategic Advisory Group</strong> that ensures a comprehensive,
                            ongoing and permanent dialogue between the community and the Competence Centre.
                        </li>
                    </ul>


                    </p>
                    <p class="ecl-u-type-paragraph-m">The ECCC will closely cooperate with the <strong>Network of National
                            Coordination Centres (NCCs)</strong>, one per Member State, which support the cybersecurity community at
                        national level and under certain conditions can pass on EU funding.</p>
                    <h2 class="ecl-u-type-heading-2" id="composition-and-operation-of-the-governing-board">Composition and operation of the Governing
                        Board:</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item"><strong>one representative from
                                each Member State and two representatives from the Commission</strong> (and an alternate for each
                            representative), with cybersecurity knowledge and managerial skills. They serve for a <strong>renewable term of four
                                years</strong>;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>observers</strong>, including ENISA as permanent observer, and other
                            observers on an ad-hoc basis;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>a Chairperson and a Deputy Chairperson</strong> elected among the
                            members of the Governing Board for three years, once renewable;
                        </li>
                        <li class="ecl-unordered-list__item">the <strong>Executive Director</strong> will take part in the meetings of
                            the Governing Board but shall have no right to vote.
                        </li>
                    </ul>

                    </p>
                    <h2 class="ecl-u-type-heading-2" id="decision-making">Decision making:</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item">in principle, all <strong>decisions are taken by consensus</strong> among
                            the members of the Governing Board;
                        </li>
                        <li class="ecl-unordered-list__item">where decisions cannot be taken by consensus, decisions
                            shall be taken by a <strong>majority of at least
                                75% of all votes</strong>, with every Member State and the Commission having one vote. For decisions
                            concerning the description of “joint actions” and the conditions of their implementation,
                            the vote is proportional to the financial contributions of the members participating in the
                            action;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>the Union holds 26% voting rights</strong> for decisions affecting
                            the EU budget.
                        </li>
                    </ul>

                    </p>
                    <h2 class="ecl-u-type-heading-2" id="key-functions-of-the-governing-board">Key functions of the Governing Board:</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item">provide <strong>strategic orientations and oversee the Centre’s
                                activities</strong>;
                        </li>
                        <li class="ecl-unordered-list__item">adopt the <strong>work programme, annual budget, consolidated
                                annual activity report</strong>;
                        </li>
                        <li class="ecl-unordered-list__item">adopt <strong>the financial rules</strong>, the <strong>anti-fraud strategy,
                                rules for the prevention and management of conflicts of interest, communication policy</strong>;
                        </li>
                        <li class="ecl-unordered-list__item">set up <strong>working groups within the community</strong>;</li>
                        <li class="ecl-unordered-list__item">appoint the Executive Director and the Accounting
                            Officer;
                        </li>
                        <li class="ecl-unordered-list__item">appoint the members of the Strategic Advisory Group.</li>
                    </ul>

                    </p>
                    <h2 class="ecl-u-type-heading-2" id="executive-director">Executive Director responsibilities:</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item"><strong>day-to-day management</strong> of the Centre;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>implementation</strong> of the tasks assigned to
                            the Centre by the Regulation;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>assisting and supporting the Governing Board</strong> on behalf of the
                            staff of the ECCC;
                        </li>
                        <li class="ecl-unordered-list__item">preparing and implementing the work programme and <strong>reporting to
                                the Governing Board</strong>.
                        </li>
                    </ul>
                    </p>
                    <h2 class="ecl-u-type-heading-2" id="national-coordination-centres">National Coordination Centres (NCCs)</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item">one NCC from each Member State;</li>
                        <li class="ecl-unordered-list__item"><strong>nominated by Member States</strong> and notified to the Commission;
                        </li>
                        <li class="ecl-unordered-list__item">possess or have access to research and technological
                            expertise in cybersecurity;
                        </li>
                        <li class="ecl-unordered-list__item">key function: <strong>national capacity building</strong>, and link with
                            existing initiatives and national cyber community;
                        </li>
                        <li class="ecl-unordered-list__item">can effectively <strong>engage and coordinate</strong> with industry,
                            academia and research community, citizens, and the public sector and authorities under NIS;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>can receive direct EU grants</strong>;</li>
                        <li class="ecl-unordered-list__item">can provide <strong>financial support</strong> to third parties.</li>
                    </ul>

                    </p>
                    <h2 class="ecl-u-type-heading-2" id="strategic-advisory-group">Strategic Advisory Group:</h2>
                    <p class="ecl-u-type-paragraph-m">
                    <ul class="ecl-unordered-list">
                        <li class="ecl-unordered-list__item"><strong>composition: 20 members</strong> appointed by the Governing Board
                            from among the representatives of the entities of the cyber community;
                        </li>
                        <li class="ecl-unordered-list__item"><strong>ensure expertise</strong> in cybersecurity research, industrial
                            development, professional services or products;
                        </li>
                        <li class="ecl-unordered-list__item">two-year term, once renewable;</li>
                        <li class="ecl-unordered-list__item">meets at least three times per year;</li>

                        <li class="ecl-unordered-list__item"><strong>Tasks:</strong>
                            <ul class="ecl-unordered-list">
                                <li class="ecl-unordered-list__item">Advises the Governing Board on establishing working
                                    groups
                                </li>
                                <li class="ecl-unordered-list__item">Organises public consultations to collect input
                                    that it provides to the Executive Director and
                                    the Governing Board with regard to the agenda, the annual work programme and the
                                    multi-annual work programme
                                </li>

                            </ul>
                        </li>
                    </ul>


                    </p>


