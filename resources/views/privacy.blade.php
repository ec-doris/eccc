                    <h2 class="ecl-u-type-heading-3" id="introduction">Introduction</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m">The European Commission (hereafter ‘the Commission’) is committed
                        to protect your personal data and to respect your privacy. The Commission collects and further
                        processes personal data pursuant to <a
                            href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=uriserv:OJ.L_.2018.295.01.0039.01.ENG&amp;toc=OJ:L:2018:295:TOC">Regulation
                            (EU) 2018/1725</a> of the European Parliament and of the Council of 23&nbsp;October 2018 on
                        the protection of natural persons with regard to the processing of personal data by the Union
                        institutions, bodies, offices and agencies and on the free movement of such data (repealing
                        Regulation (EC) No 45/2001).</p>

                    <p class="ecl-u-type-paragraph-m">This privacy statement explains the reason for the processing of
                        your personal data, the way we collect, handle and ensure protection of all personal data
                        provided, how that information is used and what rights you have in relation to your personal
                        data. It also specifies the contact details of the responsible Data Controller with whom you may
                        exercise your rights, the Data Protection Officer and the European Data Protection
                        Supervisor.</p>

                    <p class="ecl-u-type-paragraph-m">The information in relation to processing operation “Data
                        processing on European Commission web sites (within the europa.eu domain)“ undertaken by the
                        European Commission is presented below.</p>

                    <h2 id="why-and-how-do-we-process-your-personal-data" class="ecl-u-type-heading-3 ecl-u-mt-2xl">Why
                        and how do we process your personal data?</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m"><span class="ecl-u-type-m ecl-u-type-underline">Purpose of the processing operation:</span>
                        The European Commission’s Directorates-General, including the Directorate-General for
                        Communication, offer a large number of websites located on infrastructure maintained by the
                        Directorate-General for Informatics. The technical setup collects and uses your personal
                        information to be able to provide the European Commission websites within the europa.eu domain,
                        and which are physically located on the Commission controlled hosting infrastructure.</p>

                    <p class="ecl-u-type-paragraph-m">When you access a European Commission website, the European
                        Commission receives as an essential technical requirement the Internet Protocol address (IP
                        address) or the device ID of the device used to access the website. </p>

                    <p class="ecl-u-type-paragraph-m">In case of an opt-in, your data are collected via “Formtools”
                        (hosted on Commission servers) and subsequently manually copied on a spreadsheet, stored on a
                        shared drive on the operating DG network.</p>

                    <p class="ecl-u-type-paragraph-m">Your personal data will not be used for an automated
                        decision-making including profiling.</p>

                    <h2 id="on-what-legal-grounds-do-we-process-your-personal-data"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">On what legal ground(s) do we process your personal
                        data</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m">We process your personal data, because:</p>

                    <p class="ecl-u-type-paragraph-m">(a) processing is necessary for the performance of a task carried
                        out in the public interest or in the exercise of official authority vested in the Union
                        institution or body (Article 5(1)(a) of the Regulation 2018/1725).</p>
                    <p class="ecl-u-type-paragraph-m">Informing the broad public is a task resulting from the European
                        Commission's own prerogatives at institutional level, as provided for in Article 58(2) (d) of
                        Council Regulation (EC, Euratom) No 2018/1046 of 18 July 2018 on the Financial Regulation
                        applicable to the general budget of the European Communities (OJ L 193, 30.7.2018, p. 1)We
                        ensure that adequate and specific safeguards are implemented for the processing of personal
                        data, in line with the applicable data protection legislation. </p>
                    <p class="ecl-u-type-paragraph-m">(b) For specific processing activities, the consent of the data
                        subject is necessary. In compliance with Article 3(15) and Article 7 of Regulation (EU)
                        2018/1725, the consent must be freely given, specific, informed and unambiguous.</p>
                    <p class="ecl-u-type-paragraph-m">You have given <span class="ecl-u-type-m ecl-u-type-underline">consent</span>
                        to the processing of your personal data for one or more specific purposes (Article 5(1)(d) of
                        Regulation (EU) 2018/1725).</p>
                    <p class="ecl-u-type-paragraph-m"><span class="ecl-u-type-m ecl-u-type-underline">We have obtained your consent directly from you. You may have expressed it by an email, submitted via e-registration form, or in any other written form.</span>
                    </p>

                    <h2 id="which-personal-data-do-we-collect-and-further-process"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">Which personal data do we collect and further
                        process?</h2>
                    <hr>


                    <p class="ecl-u-type-paragraph-m">In order to carry out this processing operation the
                        Directorate-General for Informatics collects the following categories of personal data:</p>
                    <p class="ecl-u-type-paragraph-m">Internet Protocol address (IP address) or the device ID of the
                        device used to access the website.</p>
                    <p class="ecl-u-type-paragraph-m">Without this processing you will not be able to establish a
                        technical connection between your devices and the server infrastructure maintained by the
                        European Commission and therefore will not be able to access the websites of the European
                        Commission.</p>


                    <h2 id="how-long-do-we-keep-your-personal-data" class="ecl-u-type-heading-3 ecl-u-mt-2xl">How long
                        do we keep your personal data?</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m">The European Commission’s Directorate-General for Informatics only
                        keeps your personal data for the time necessary to fulfil the purpose of collection or further
                        processing, namely for the duration of the browsing session. In addition, IP addresses might be
                        saved for one year in the log files of the Directorate-General for Informatics operational
                        environment for security or other purposes (ref. DPR-EC-02886 DIGIT IT security operations and
                        services).</p>

                    <h2 id="how-do-we-protect-and-safeguard-your-personal-data"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">How do we protect and safeguard
                        your personal data?</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m">All personal data in electronic format (e-mails, documents,
                        databases, uploaded batches of data, etc.) are stored either on the servers of the European
                        Commission or of its contractors. All processing operations are carried out pursuant to the
                        <a
                            href="https://eur-lex.europa.eu/legal-content/EN/TXT/?qid=1548093747090&uri=CELEX:32017D0046">Commission
                            Decision (EU, Euratom) 2017/46</a> of 10 January 2017 on the security of communication
                        and information systems in the European Commission.</p>

                    <p class="ecl-u-type-paragraph-m">The Commission’s contractors are bound by a specific contractual
                        clause for any processing operations of your data on behalf of the Commission, and by the
                        confidentiality obligations deriving from the transposition of the General Data Protection
                        Regulation in the EU Member States (‘GDPR’ <a
                            href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=celex:32016R0679">Regulation (EU)
                            2016/679)</a>.</p>


                    <p class="ecl-u-type-paragraph-m">In order to protect your personal data, the Commission has put in
                        place a number of technical and organisational measures. Technical measures include appropriate
                        actions to address online security, risk of data loss, alteration of data or unauthorised
                        access, taking into consideration the risk presented by the processing and the nature of the
                        personal data being processed. Organisational measures include restricting access to the
                        personal data solely to authorised persons with a legitimate need to know for the purposes of
                        this processing operation.</p>

                    <h2 id="who-has-access-to-your-personal-data-and-to-whom-is-it-disclosed"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">Who has access to your personal data and to whom is it disclosed?</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m">Access to your personal data is provided to the Commission staff responsible for carrying out this processing operation and to authorised staff according to the “need to know” principle. Such staff abide by statutory, and when required, additional confidentiality agreements.</p>
                    <p class="ecl-u-type-paragraph-m">Furthermore, access to your personal data can be provided to external contractors working on behalf of and under contractual agreement with the Commission service owning the websites and engaged in creation, maintenance, management, and archiving of websites according to the “need to know” principle. </p>
                    <p class="ecl-u-type-paragraph-m">The information we collect will not be given to any third party, except to the extent and for the purpose we may be required to do so by law.</p>


                    <h2 id="what-are-your-rights-and-how-can-you-exercise-them"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">What are your rights and how
                        can you exercise them?</h2>
                    <hr>


                    <p class="ecl-u-type-paragraph-m">You have specific rights as a ‘data subject’ under Chapter III (Articles 14-25) of Regulation (EU) 2018/1725, in particular the right to access, rectify or erase your personal data and the right to restrict the processing of your personal data. Where applicable, you also have the right to object to the processing or the right to data portability.</p>
                    <p class="ecl-u-type-paragraph-m">You have the right to object to the processing of your personal data, which is lawfully carried out pursuant to Article 5(1)(a).</p>
                    <p class="ecl-u-type-paragraph-m">You have consented to provide your personal data for the present processing operation. You can withdraw your consent at any time by notifying the Data Controller. The withdrawal will not affect the lawfulness of the processing carried out before you withdrew your consent.</p>
                    <p class="ecl-u-type-paragraph-m">You can exercise your rights by contacting the Data Controller, or in case of conflict the Data Protection Officer. If necessary, you can also address the European Data Protection Supervisor. Their contact information is given under Heading 9 below.</p>
                    <p class="ecl-u-type-paragraph-m">Where you wish to exercise your rights in the context of one or several specific processing operations, please provide their description (i.e. their Record reference(s) as specified under Heading 10 below) in your request.</p>





                    <h2 id="contact-information"
                        class="ecl-u-type-heading-3 ecl-u-mt-2xl">Contact information</h2>
                    <hr>

                    <p class="ecl-u-type-paragraph-m"><strong>The Data Controller</strong></p>

                    <p class="ecl-u-type-paragraph-m">If you would like to exercise your rights under Regulation (EU) 2018/1725, or if you have comments, questions or concerns, or if you would like to submit a complaint regarding the collection and use of your personal data, please feel free to contact the Data Controller, whose contact details will be provided in the specific privacy statement linked to the website.</p>

                    <p class="ecl-u-type-paragraph-m"><strong>The Data Protection Officer (DPO) of the
                            Commission</strong></p>

                    <p class="ecl-u-type-paragraph-m">You may contact the Data Protection Officer (<a
                            href="mailto:DATA-PROTECTION-OFFICER@ec.europa.eu">DATA-PROTECTION-OFFICER@ec.europa.eu</a>)
                        with regard to issues related to the processing of your personal data under Regulation (EU)
                        2018/1725.</p>

                    <p class="ecl-u-type-paragraph-m"><strong>The European Data Protection Supervisor (EDPS)</strong>
                    </p>

                    <p class="ecl-u-type-paragraph-m">You have the right to have recourse (i.e. you can lodge a
                        complaint) to the European Data Protection Supervisor (<a href="mailto:edps@edps.europa.eu">edps@edps.europa.eu</a>)
                        if you consider that your rights under Regulation (EU) 2018/1725 have been infringed as a result
                        of the processing of your personal data by the Data Controller.</p>


