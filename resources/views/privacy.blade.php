                            <p class="ecl-u-type-paragraph-m"><strong>Processing operation:</strong> Processing of
                                personal data on European Commission web sites
                                (within the europa.eu domain), including IP addresses, by visitors of the publicly
                                available websites.</p>
                            <p class="ecl-u-type-paragraph-m"><strong>Data Controller:</strong> European Commission</p>
